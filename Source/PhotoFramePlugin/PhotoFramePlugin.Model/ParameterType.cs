        /// Ширина рамки.
        /// </summary>
        FrameWidth,

        /// <summary>
        /// Высота рамки.
        /// </summary>
        FrameHeight,

        /// <summary>
        /// Толщина рамки.
        /// </summary>
        FrameThickness,

        /// <summary>
        /// Толщина задней стенки.
        /// </summary>
        BackWallThickness,

        /// <summary>
        /// Скругление рамки.
        /// </summary>
        FrameRounding,
    }
}